const fruits = ["Apple", "Banana", "Orange"];

console.log(fruits.join());
// Output:
// Apple,Banana,Orange

const words = ["JavaScript", "is", "fun"];
console.log(words.join(" "))
// Output:
// JavaScript is fun

const data = ["Name", "Age", "Location"];
console.log(data.join(" | "))
// Output:
// Name | Age | Location